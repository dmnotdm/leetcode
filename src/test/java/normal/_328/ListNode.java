package normal._328;

/**
 * Definition for singly-linked list.
 *
 * @author zhizhao.zhang
 * @date 2020/11/13
 */
class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}